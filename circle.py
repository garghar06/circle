class Circle():
    def __init__(self,r):
        self.radius=r
    
    def area(Self):
        areaOfCircle=3.14*Self.radius**2
        return areaOfCircle
    
    def perimeter(Self):
        peri=2*Self.radius*3.14
        return peri
    
NewCircle=Circle(4)
print(NewCircle.area())
print(NewCircle.perimeter())

